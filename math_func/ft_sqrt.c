#include "libft.h"

int		ft_sqrt(int nb)
{
	int sqrt;

	sqrt = 0;
	if (nb < 0)
		return (0);
	while (sqrt * sqrt < nb && sqrt < 46341)
	{
		sqrt++;
	}
	return (sqrt);
}
